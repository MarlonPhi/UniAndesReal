package co.edu.uniandes.fuse.api.academico.models.entity;

public class Becado {
	
	  private int sPidm;
	  private String sCodigoBeca;
	  private String sDescripcionBeca;
	  private String sSemestre;
	  
	  
	  
	public int getsPidm() {
		return sPidm;
	}
	public void setsPidm(int sPidm) {
		this.sPidm = sPidm;
	}
	public String getsCodigoBeca() {
		return sCodigoBeca;
	}
	public void setsCodigoBeca(String sCodigoBeca) {
		this.sCodigoBeca = sCodigoBeca;
	}
	public String getsDescripcionBeca() {
		return sDescripcionBeca;
	}
	public void setsDescripcionBeca(String sDescripcionBeca) {
		this.sDescripcionBeca = sDescripcionBeca;
	}
	public String getsSemestre() {
		return sSemestre;
	}
	public void setsSemestre(String sSemestre) {
		this.sSemestre = sSemestre;
	}
	  
	  
	  

}
